def init_quiz_db() -> None:
    with db.connect() as conn:
        conn.execute("""
            CREATE TABLE IF NOT EXISTS quiz_results (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                chat_id INTEGER NOT NULL,
                date TEXT NOT NULL,
                topic TEXT NOT NULL,
                correct INTEGER NOT NULL,
                total INTEGER NOT NULL
            )
        """)


def log_quiz_result(chat_id: int, topic: str, correct: int, total: int) -> None:
    today = datetime.now(TZ).strftime("%Y-%m-%d")
    with db.connect() as conn:
        conn.execute(
            "INSERT INTO quiz_results (chat_id, date, topic, correct, total) VALUES (?, ?, ?, ?, ?)",
            (chat_id, today, topic, correct, total),
        )


def get_questions(topic: str) -> List[Dict[str, Any]]:
    pool = QUESTIONS.get(topic, [])
    count = min(QUIZ_SIZE, len(pool))
    return random.sample(pool, count)


def format_question(question: Dict[str, Any], idx: int, total: int) -> str:
    letters = ["A", "B", "C", "D"]
    options_text = "\n".join(
        f"{letters[i]}. {opt}" for i, opt in enumerate(question["options"])
    )
    return f"Вопрос {idx + 1}/{total}\n\n{question['q']}\n\n{options_text}"


def format_answer_result(question: Dict[str, Any], chosen_idx: int) -> str:
    correct_idx = question["correct"]
    letters = ["A", "B", "C", "D"]
    chosen_letter = letters[chosen_idx]
    correct_letter = letters[correct_idx]

    if chosen_idx == correct_idx:
        verdict = f"✅ Правильно! ({chosen_letter})"
    else:
        verdict = f"❌ Неверно. Ты выбрал {chosen_letter}, правильный ответ — {correct_letter}."

    return f"{verdict}\n\n{question['explanation']}"


def format_score(correct: int, total: int, topic_label: str) -> str:
    pct = int(correct / total * 100) if total else 0
    if pct == 100:
        grade = "Отлично! Тема освоена."
    elif pct >= 60:
        grade = "Хорошо. Есть над чем поработать."
    else:
        grade = "Нужно повторить тему."

    return (
        f"Квиз завершён: {topic_label}\n\n"
        f"Результат: {correct}/{total} ({pct}%)\n"
        f"{grade}"
    )
